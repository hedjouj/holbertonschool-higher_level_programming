#!/usr/bin/python3
"""function that returns true if the object is an instance or if the object
is an instance of a class that inherited from, otherwise false
"""


def is_kind_of_class(obj, a_class):
    """"
    returns true or false depending on the object
    """
    if isinstance(obj, a_class):
        return True
    else:
        if isinstance(obj, a_class):
            return False
